void solve_tc(){
    int n; cin >> n;

    int ans = 1;
    if (n == 2) ans = 9;
    else if (n == 3 || n == 4) ans = 4 * n * n - n - 4;
    else if (n > 4) ans = 5 * n * n - 5 * n - 5;

    cout << ans << endl;
}

